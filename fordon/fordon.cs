class Fordon
{
    private string märke;
    private string modell;
    private int årsmodell;

    public string Märke
    {
        get { return märke; }
        set { märke = value; }
    }

    public string Modell
    {
        get { return modell; }
        set { modell = value; }
    }
    
    public int Årsmodell
    {
        get { return årsmodell; }
        set { årsmodell = value; }
    }
}